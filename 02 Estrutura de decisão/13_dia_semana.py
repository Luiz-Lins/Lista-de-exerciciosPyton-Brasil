

numero = int(input('Digite um número: '))

if numero == 1:
    print('Domingo')
elif numero == 2:
    print('Seg')
elif numero == 3:
    print('Ter')
elif numero == 5:
    print('Qua')
elif numero == 6:
    print('Qui')
elif numero == 7:
    print('Sex')
elif numero == 8:
    print('Sab')
else:
    print('Valor inválido')